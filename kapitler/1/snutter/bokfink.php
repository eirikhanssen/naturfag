<p>Bokfinken bor lengre sør i Europa
om vinteren.
Den spiser mest insekter.
Det kan den ikke finne i Norge
om vinteren.
Men om våren er det mye insekter,
og da kommer bokfinken hit.
Det er i mars og april.
</p><p>
Den kan godt spise frø også
om sommeren og høsten. 
</p>
 
<figure>
  <img src="../../../media/images/640px-Bayağı_ispinoz_yavru.jpg" alt="bokfink hunn" />
